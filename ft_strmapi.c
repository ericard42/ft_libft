#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*dest;
	size_t	i;
	size_t	size;

	if (s == NULL || f == NULL)
		return (NULL);
	size = ft_strlen(s);
	if (!(dest = (char*)malloc(sizeof(*dest) * (size + 1))))
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		dest[i] = f(i, s[i]);
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
